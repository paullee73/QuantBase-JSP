package mypackage;

public class User {
	private String name;
	private String password;
	
	public void setName(String nm){
		this.name = nm;
	}
	
	public void setPassword(String ps){
		this.password = ps;
	}
	
	public String getName(){
		return name;
	}
	
	public String getPassword(){
		return password;
	}
}
